package Ex;
//switch문을 if~else 문으로 변경하기.
public class Ex04 {

	public static void main(String[] args) {
		int n = 1;
		if (n == 1) {
			System.out.println("Simple Java");
		} else if (n == 2) {
			System.out.println("Funny Java");
		} else if (n == 3) {
			System.out.println("Fantastic Java");
		} else {
			System.out.println("The best programming");
		}
	}
}
