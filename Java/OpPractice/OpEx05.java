package op;
//~의 용도 , 질문사항!!
public class OpEx05 {
	public static void main(String[] args) {
		byte b=-9;
		System.out.println("b="+b);
		System.out.println("~b="+~b); //부호 반대 , -1연산
		System.out.println("~b+1="+~(b+1));
		byte a=9;
		System.out.println("a="+a);
		System.out.println("~a="+~a); //부호 반대, -1연산
		System.out.println("~a+1="+~(a+1));
		
	}

}

