package op;
//논리 부정의 용도와 유용성
public class OperatorEx4 {

	public static void main(String[] args) {
		
		boolean power = false;
		
		System.out.println(power);
		
		
		power = !power; //논리부정!로 인해 power의 값이 false에서 true로 바뀐다.
		
		
		System.out.println(power);
		
		power = !power;   //논리부정!로 인해 power의 값이 false에서 true로 바뀐다
		
		System.out.println(power); 
		
		}
	
}
		

