package ex1;

public class UseVariable {

	public static void main(String[] args) {
		
		//정수형 변수 num1을 선언한다.
		int num1;
		//변수값에 10을 저장한다.
		num1 = 10;
		
		int num2 = 20; // 변수 선언과 초기화를 한 번에 정의
		
		int num3 = num1 + num2;
		
		//정수 , 문자열 출력의 예
		System.out.println(num1+ "+" +num2+ "=" +num3);
		

	}

}

