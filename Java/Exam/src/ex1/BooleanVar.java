package ex1;

public class BooleanVar {
	public static void main(String[] args) {
		
		boolean b1 = true;
		boolean b2 = false;
		
		int num = 100; //임의의 변수
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("----------------------------");
		System.out.println(4>3); // > , <, == . >= , <=
		System.out.println(3>4);
		System.out.println("----------------------------");
		System.out.println(num>3); //시스템의 흐름을 만들어 줄 수 있다.
		//예를 들면 3세 이상의 사람들에게는 어떤 것 지정.
		
		
	}

}

